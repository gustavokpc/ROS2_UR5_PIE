function J = calibrationCost(p, Tbg, Tcg)
    X_gc = vec2T(p(1:6));
    X_bc = vec2T(p(7:12));

    N = size(Tbg,3);
    J = 0;

    for i = 1:N
        E = Tbg(:,:,i) * X_gc - X_bc * Tcg(:,:,i);

        % peso um pouco maior na translacao, mas sem exagerar
        Er = E(1:3,1:3);
        Et = E(1:3,4);

        J = J + norm(Er,'fro')^2 + 10*norm(Et)^2;
    end
end

function T = vec2T(v)
    r = v(1:3);
    t = v(4:6);

    R = rodriguesToRotm(r);

    T = eye(4);
    T(1:3,1:3) = R;
    T(1:3,4) = t;
end

function R = rodriguesToRotm(r)
    theta = norm(r);

    if theta < 1e-12
        R = eye(3);
        return;
    end

    k = r / theta;
    K = skew3(k);

    R = eye(3) + sin(theta)*K + (1-cos(theta))*(K*K);
end

function S = skew3(v)
    S = [   0   -v(3)  v(2);
          v(3)   0    -v(1);
         -v(2) v(1)    0  ];
end

function T = robotPose2T(pose)
    % pose = [tz ty tx x y z]

    tz = pose(1);
    ty = pose(2);
    tx = pose(3);
    x  = pose(4);
    y  = pose(5);
    z  = pose(6);

    Rz = [cos(tz) -sin(tz) 0;
          sin(tz)  cos(tz) 0;
             0        0    1];

    Ry = [ cos(ty) 0 sin(ty);
              0    1   0;
          -sin(ty) 0 cos(ty)];

    Rx = [1 0 0;
          0 cos(tx) -sin(tx);
          0 sin(tx)  cos(tx)];

    R = Rz * Ry * Rx;

    T = [R [x; y; z];
         0 0 0 1];
end

function T = cameraPose2T(pose)
    % pose = [qx qy qz qw x y z]

    qx = pose(1);
    qy = pose(2);
    qz = pose(3);
    qw = pose(4);
    x  = pose(5);
    y  = pose(6);
    z  = pose(7);

    q = [qw qx qy qz];
    q = q / norm(q);

    qw = q(1);
    qx = q(2);
    qy = q(3);
    qz = q(4);

    R = [1-2*(qy^2+qz^2),   2*(qx*qy-qz*qw),   2*(qx*qz+qy*qw);
         2*(qx*qy+qz*qw), 1-2*(qx^2+qz^2),   2*(qy*qz-qx*qw);
         2*(qx*qz-qy*qw),   2*(qy*qz+qx*qw), 1-2*(qx^2+qy^2)];

    T = [R [x; y; z];
         0 0 0 1];
end







